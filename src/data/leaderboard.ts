export function getLeaderboardByCategory(
  category: LeaderboardCategory,
  month?: string
): LeaderboardEntry[] {
  return MOCK_LEADERBOARD.filter(
    (e) =>
      e.category === category && (month ? e.month === month : true)
  ).sort((a, b) => a.rank - b.rank);
}

export function getSkillRanking(skill: string): SkillRanking | undefined {
  return SKILL_RANKINGS.find(
    (s) => s.skill.toLowerCase() === skill.toLowerCase()
  );
}

export function searchSkills(query: string): { id: LeaderboardCategory; label: string; icon: string }[] {
  const q = query.toLowerCase().trim();
  if (!q) return SKILL_CATEGORIES;
  return SKILL_CATEGORIES.filter(
    (s) => s.label.toLowerCase().includes(q)
  );
}
